void main() {
  // Khai báo các kiểu dữ liệu
  int soNguyen = 24; // Số nguyên
  double soThuc = 3.14; // Số thực
  String chuoiKyTu = "Chú khỉ ko vui!"; // Chuỗi ký tự
  bool giaTriBoolean = true; // Giá trị boolean

  // In ra giá trị của các biến
  print("Số nguyên: $soNguyen");
  print("Số thực: $soThuc");
  print("Chuỗi ký tự: $chuoiKyTu");
  print("Giá trị boolean: $giaTriBoolean");
}